const DICTIONARIES: Record<Language, Dictionary> = { en, ru };

function interpolate(template: string, params?: Record<string, string | number>): string {
  if (!params) return template;
  return template.replace(/\{\{(\w+)\}\}/g, (_, key: string) => String(params[key] ?? ''));
}

export function translate(language: Language, key: string, params?: Record<string, string | number>): string {
  const template = DICTIONARIES[language][key] ?? DICTIONARIES.en[key] ?? key;
  return interpolate(template, params);
}

/** React hook: returns `t(key, params?)` bound to the current UI language setting. */
export function useT() {
  const language = useSettingsStore((state) => state.language);
  return (key: string, params?: Record<string, string | number>) => translate(language, key, params);
}
